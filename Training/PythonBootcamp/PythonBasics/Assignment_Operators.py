# Equals(=)
a = 5
# Plus equals(+=)
a += 2 # shorthand for a = a + 2 => a is 7
print(a)
# minus equals(-=)
a -= 3 # shorthand for a = a - 3 => a is 4
print(a)
# star equals (*=)
a *= 3 # a =a * 3 => a is 12
print(a)
# slash equals (/=)
a /= 2 # a = a / 2 => a is 6
print(a)
# double stars equals (**=)
a **= 2 # a = a ** 2 => a is 36
print(a)

a += 1 # for incrementing

# divmode()
a, b = divmod(14,6)
print(a, b)
# pow()
print(pow(5, 9))
# sum()
print(sum([1, 2, 3,4]))
# max()
print(max([1, 2, 3, 4]))
# min()
print(min([1, 2, 3, 4]))
# round()
a = 5.262442
print(round(a,3))
b = round(a, 1)
print(a, b)